    <p>
        a freshwater lake in Kenya, outside the town of Naivasha in Nakuru County, which lies north west of Nairobi. It is part of the Great Rift Valley. 
        The name derives from the local Maasai name ɛnaɨpɔ́sha , meaning "that which heaves," a common Maasai word for bodies of water larger enough to have wave action when it is windy or stormy. 
        Naivasha arose as the British attempt to pronounce the Maasai name. Literally, Lake Naivasha means "Lake Lake."<br>

Read more about Lake Naivasha<br>
Lake Naivasha is at the highest elevation of the Kenyan Rift valley at 1,884 metres (6,181 ft) in a complex geological combination of volcanic rocks and sedimentary deposits from a larger Pleistocene Era lake. Apart from transient streams, the lake is fed by the perennial Malewa and Gilgil rivers. There is no visible outlet, but since the lake water is relatively fresh it is assumed to have an underground outflow.[2]

The lake had a normal surface area of 139 square kilometres (54 sq mi)[3] before 2010 and had increased to 198 square kilometres (76 sq mi) by 2020.
[4] It is surrounded by a swamp which covers an area of 64 square kilometres (25 sq mi), but this can vary largely depending on rainfall. It is situated at an altitude of 1,884 metres (6,181 ft).
[3] The lake has an average depth of 6 metres (20 ft), with the deepest area being at Crescent Island, at a maximum depth of 30 metres (98 ft).[3] Njorowa Gorge once formed the lake's outlet, but it is now high above the lake and forms the entrance to Hell's Gate National Park. 
The town of Naivasha (formerly East Nakuru) lies on the north-east edge of the lake.



