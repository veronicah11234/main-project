        Lake Naivasha, once described as the Jewel in the Crown of all the East African lakes, has in recent decades been subjected to a series of devastating human pressures, not least the establishment of a vast horticulture and agriculture industry along its shorelines, but also the ever-increasing inflows of nutrients from siltation, sewage and other effluents emanating from a lakeside human population now approaching a million people.
        The water level for Lake Naivasha reached a low of 0.6 m depth in 1945, but the water level rose again, with minor drops, to reach a maximum depth of nearly 6 m in 1968.[3] There was another major decline of the water level in 1987, when the depth reached 2.25 m above the lake bottom.[3] The decline of the lake water level in 1987 increased concern in the future of geothermal industry, and it was speculated that Lake Naivasha underground water might be feeding the geothermal reservoir at Olkaria.
